void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	long i;

	i = nb;
	if (nb < 0)
	{
		ft_putchar('-');
		i *= -1;
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putnbr(i % 10);
	}
	else
		ft_putchar(i + '0');
}
